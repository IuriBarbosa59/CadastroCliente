public class Endereco {
    String NomeLo;
    int Numero;
    String Bairro;
    String Cidade;
    String Estado;

    Endereco(String NomeLo, int Numero, String Bairro, String Cidade, String Estado) {
        this.NomeLo = NomeLo;
        this.Numero = Numero;
        this.Bairro = Bairro;
        this.Cidade = Cidade;
        this.Estado = Estado;
    }

}
