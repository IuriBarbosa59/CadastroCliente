public class Cliente {

    String Nome;
    int Rg;
    int Cpf;
    int Telefone;
    int DataNasc;
    String Endereco;

    Cliente(String Nome, int Rg, int Cpf, int Telefone, int DataNasc, String Endereco) {
        this.Nome = Nome;
        this.Rg = Rg;
        this.Cpf = Cpf;
        this.Telefone = Telefone;
        this.DataNasc = DataNasc;
        this.Endereco = Endereco;
    }

}
